/**
 * Render publish flow.
 *
 * - generateRenderYaml(project): produces the YAML string for the project.
 * - commitRenderYaml(): writes render.yaml into the project's working dir,
 *   pushes via the existing auto-sync git mechanics so it lands on the
 *   student's GitHub repo, and marks the project row.
 * - buildDeployUrl(): returns the Render Blueprint deploy URL.
 * - predictedSiteUrl(): returns the URL the deployed site WILL have once
 *   Render finishes building.
 *
 * We don't talk to Render's API at all. Render reads render.yaml from the
 * student's repo when they click Create on the deploy page, and from then on
 * Render's GitHub webhook handles all redeploys.
 */

import { execFile } from "node:child_process";
import { promisify } from "node:util";
import { writeFile } from "node:fs/promises";
import { join } from "node:path";
import {
  getGithubConnection,
  getProjectById,
  markRenderYamlCommitted,
  type ProjectRow,
} from "./db.ts";
import { decrypt } from "./crypto.ts";
import { getAppCredentials } from "./github-app.ts";
import { WORKING_BRANCH } from "./github-sync.ts";
import { Octokit } from "@octokit/rest";
import { createAppAuth } from "@octokit/auth-app";
import { log } from "./log.ts";

const exec = promisify(execFile);

// ── render.yaml content ──────────────────────────────────────────────────────

/** The Render service name = student's repo slug, prefixed for namespace clarity. */
export function renderServiceName(project: ProjectRow): string {
  // Project slugs already include a 6+ char hash suffix (Lovable-style), so
  // they're effectively unique on Render's `<name>.onrender.com` global
  // namespace at low scale.
  return project.slug;
}

export function generateRenderYaml(project: ProjectRow): string {
  const name = renderServiceName(project);
  return `# Render blueprint for ${project.display_name}
# Generated by Cloudwise Lab. Render reads this file on every push and
# redeploys automatically. Edit at your own risk; the lab won't overwrite it.

services:
  - type: web
    name: ${name}
    runtime: static
    buildCommand: npm install && npm run build
    staticPublishPath: ./dist
    branch: ${project.github_default_branch ?? "main"}
    autoDeploy: true
    pullRequestPreviewsEnabled: true
`;
}

export function predictedSiteUrl(project: ProjectRow): string {
  return `https://${renderServiceName(project)}.onrender.com`;
}

export function buildDeployUrl(project: ProjectRow): string {
  if (!project.github_repo_full_name) {
    throw new Error("project has no GitHub repo connected");
  }
  const repoUrl = `https://github.com/${project.github_repo_full_name}`;
  return `https://render.com/deploy?repo=${encodeURIComponent(repoUrl)}`;
}

// ── Commit render.yaml to the student's repo ─────────────────────────────────

/**
 * Writes render.yaml into the project's working dir, commits it, and pushes to
 * the student's GitHub repo. Re-runnable: if the file already matches what's
 * on disk, this is a no-op. We never overwrite a render.yaml the student has
 * customized — only create it if missing or if it's still our generated one.
 */
export async function commitRenderYaml(input: {
  userId: number;
  projectId: number;
  projectDir: string;
}): Promise<void> {
  const project = getProjectById(input.projectId);
  if (!project || project.user_id !== input.userId) {
    throw new Error("project not found");
  }
  if (!project.github_repo_full_name) {
    throw new Error("connect GitHub before publishing");
  }

  const conn = getGithubConnection(input.userId);
  if (!conn) throw new Error("GitHub not connected");

  const yamlContent = generateRenderYaml(project);
  const yamlPath = join(input.projectDir, "render.yaml");
  await writeFile(yamlPath, yamlContent, "utf-8");

  // Use a fresh installation token (server-to-server, scoped to the install)
  // for the push — same pattern as auto-sync.
  const { appId, privateKey } = getAppCredentials();
  const auth = createAppAuth({ appId, privateKey });
  const tokenRes = await auth({
    type: "installation",
    installationId: conn.installation_id,
  });
  const token = (tokenRes as any).token as string;

  // render.yaml is committed to the WORKING branch (cloudwise-lab) — the same
  // branch where every agent edit lands. Publish promotes it to main.
  const remoteUrl = `https://x-access-token:${token}@github.com/${project.github_repo_full_name}.git`;
  const env = { ...process.env, GIT_TERMINAL_PROMPT: "0" };

  // Make sure we're on the working branch.
  let current = "";
  try {
    const r = await git(input.projectDir, ["rev-parse", "--abbrev-ref", "HEAD"], env);
    current = r.stdout.trim();
  } catch {}
  if (current !== WORKING_BRANCH) {
    try {
      await git(input.projectDir, ["checkout", WORKING_BRANCH], env);
    } catch {
      await git(input.projectDir, ["checkout", "-b", WORKING_BRANCH], env);
    }
  }

  await git(input.projectDir, ["add", "render.yaml"], env);
  const status = await git(input.projectDir, ["status", "--porcelain"], env);
  if (!status.stdout.trim()) {
    log.info("render.yaml already committed, no-op", {
      projectId: project.id,
    });
    markRenderYamlCommitted(project.id);
    return;
  }

  await git(
    input.projectDir,
    [
      "-c",
      "user.email=lab@cloudwise.academy",
      "-c",
      "user.name=Cloudwise Lab",
      "commit",
      "-m",
      "Add render.yaml for Render deploy",
    ],
    env
  );
  await git(input.projectDir, ["remote", "set-url", "origin", remoteUrl], env);
  // Push to the working branch; it gets promoted to main on Publish.
  await git(input.projectDir, ["push", "origin", WORKING_BRANCH], env);

  markRenderYamlCommitted(project.id);
  log.info("render.yaml committed to working branch", {
    projectId: project.id,
    branch: WORKING_BRANCH,
  });
}

// ── git helper ───────────────────────────────────────────────────────────────

async function git(
  cwd: string,
  args: string[],
  env: NodeJS.ProcessEnv
): Promise<{ stdout: string; stderr: string }> {
  return exec("git", args, { cwd, env, maxBuffer: 16 * 1024 * 1024 });
}
